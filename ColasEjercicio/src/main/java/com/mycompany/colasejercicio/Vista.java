    private void atenderClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atenderClienteActionPerformed
        
        if(lista.peek()!=null){
            dinero += Integer.parseInt(JOptionPane.showInputDialog("Valor a cobrar para: "+lista.poll()));
        }else{
            JOptionPane.showMessageDialog(null,"Todos los clientes atendidos");
        }
        
    }//GEN-LAST:event_atenderClienteActionPerformed

    private void verSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verSiguienteActionPerformed
        if(lista.peek()!=null){
            JOptionPane.showMessageDialog(null,"Siguiente en la lista: "+lista.peek());
        }else{
            JOptionPane.showMessageDialog(null,"No hay siguiente");
        }
        
    }//GEN-LAST:event_verSiguienteActionPerformed

    private void revisarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_revisarClientesActionPerformed
        if(lista.peek()!=null){
            JOptionPane.showMessageDialog(null,lista);
        }else{
            JOptionPane.showMessageDialog(null,"Lista Vac√≠a");
        }
    }//GEN-LAST:event_revisarClientesActionPerformed

